package day04_concatenation;

public class ArithmeticOperators {

    public static void main(String[] args) {

        System.out.println(100 + 200);

        System.out.println(40 - 10);

        System.out.println(50 / 6); // 8

        System.out.println(50 / 6.); // 8.333...

        System.out.println(50.0 / 6); // 8.333...

        System.out.println(50D / 6); // 8.333...

        System.out.println((int) (2.5 / 0.5)); // 5

//        System.out.println(9 / 0); // ArithmeticException

        System.out.println(10 * 2);

        System.out.println(20 * 12);

        System.out.println(100 % 13);

        System.out.println(45 % 8);

    }
}
